//basic arthmetic operators are: +, -, *, /, %
//Note: Modulo(%) operator returns remainder, for exampel 4%2 would return 0

public class ArthmeticOperator {

	public static void main(String args[]) {

		int num1 = 100;
		int num2 = 20;

		System.out.println("num1 + num2: " + (num1 + num2) );
		System.out.println("num1 - num2: " + (num1 - num2) );
		System.out.println("num1 * num2: " + (num1 * num2) );
		System.out.println("num1 / num2: " + (num1 / num2) );
		System.out.println("num1 % num2: " + (num1 % num2) );
	}
}